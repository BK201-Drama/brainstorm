"""
故事生成模块
使用现有的skills生成故事内容
"""
import os
import sys

# 添加skills目录到路径
sys.path.append(os.path.join(os.path.dirname(__file__), '..', 'skills'))


class StoryGenerator:
    def __init__(self):
        self.min_words = 800
        self.max_words = 1500
        
    def generate_story(self, topic=None, style="故事会"):
        """
        生成故事文本
        
        Args:
            topic: 故事主题（可选）
            style: 故事风格
        
        Returns:
            故事文本
        """
        # 如果没有指定主题，使用默认主题
        if topic is None:
            topic = "中年人的情感故事"
        
        # 这里可以调用现有的skill，或者使用简单的模板
        # 由于skill是Claude的，我们先用模板生成，实际使用时可以调用API
        
        story_template = f"""
{topic}

这是一个关于普通人的真实故事。

故事的主人公叫李明，今年45岁，在一家小公司做销售。每天的生活就是上班、下班、回家，三点一线，平淡无奇。

直到有一天，他遇到了一个改变他人生的人。

那是一个普通的周末下午，李明像往常一样去附近的公园散步。公园里人不多，只有几个老人在下棋，几个孩子在玩耍。

李明找了个长椅坐下，看着远处的夕阳，心里有些感慨。人到中年，事业没有太大起色，生活也缺乏激情。

就在这时，一个声音打断了他的思绪。

"你好，可以坐这里吗？"

李明抬头一看，是一个看起来和他年纪相仿的女人，穿着简单但很干净，脸上带着温和的笑容。

"当然可以。"李明往旁边挪了挪。

女人坐下后，两人开始聊天。原来她叫王芳，是一名小学老师，也是一个人来公园散心。

"你看起来有些心事？"王芳问道。

李明愣了一下，没想到一个陌生人会这么直接。但不知道为什么，他觉得很愿意和这个人聊天。

"是啊，感觉生活没什么意思。"李明说。

"我理解。"王芳点点头，"我也有过这样的感觉。但是你知道吗？生活其实有很多美好的地方，只是我们有时候太忙了，没有注意到。"

"比如呢？"李明问。

"比如今天的夕阳，比如孩子们的笑声，比如我们现在的对话。"王芳笑着说。

李明看了看周围，确实，夕阳很美，孩子们的笑声也很动听。他突然意识到，自己已经很久没有这样静下心来感受生活了。

"谢谢你。"李明说。

"不用谢。"王芳说，"其实，每个人都需要有人倾听。如果你愿意，我们可以经常来这里聊天。"

从那天开始，李明和王芳成了朋友。他们每周都会在公园见面，聊生活，聊工作，聊梦想。

李明发现，和王芳聊天让他重新找到了生活的意义。他开始关注身边的美好，开始尝试新的事物，开始对生活有了新的期待。

半年后，李明的生活发生了很大的变化。他不再觉得生活无趣，而是充满了希望和可能性。

"你知道吗？"有一天，李明对王芳说，"遇到你，是我人生中最幸运的事情之一。"

王芳笑了："我也是。有时候，我们需要的只是一个倾听者，一个能理解我们的人。"

这个故事告诉我们，人生中总会有一些意外的相遇，这些相遇可能会改变我们的人生。重要的是，我们要保持开放的心态，愿意去接受这些美好的可能性。

生活不会一直平淡，只要我们愿意去寻找，愿意去感受，总会发现那些让我们心动的人和事。
"""
        
        return story_template.strip()
    
    def generate_story_from_prompt(self, prompt):
        """
        根据提示词生成故事
        
        Args:
            prompt: 故事提示词
        
        Returns:
            故事文本
        """
        # 这里可以集成Claude API或使用现有的skill
        # 目前先用模板，实际使用时可以调用API
        story = f"""
根据以下提示生成的故事：

{prompt}

这是一个真实发生的故事。

故事的主人公是一个普通人，过着普通的生活。直到有一天，一个意外的机会改变了一切。

这个改变不是突然的，而是慢慢发生的。就像春天的花朵，一点一点地绽放。

主人公开始注意到生活中的美好，开始尝试新的事物，开始对生活有了新的理解。

在这个过程中，他遇到了很多人，经历了很多事。有些是好的，有些是困难的，但都是成长的一部分。

最终，主人公找到了属于自己的答案，找到了生活的意义。

这个故事告诉我们，每个人都有自己的路要走，重要的是要勇敢地走下去，相信美好的事情会发生。
"""
        return story.strip()
